package iLoops;

public class cancionElefantes {

    public static void main(String[] args) {

        for (int i = 1; i<=100; i++)
        {
            if (i == 1)
            {
                System.out.println(i+" se columpiaba sobre la tela de una araña como veia que resistia fue a llamar a otro elefante \n" );
            }
            else
                System.out.println(i+" se columpiaban sobre la tela de una araña como veian que resistian fueron a llamar a otro elefante \n" );
        }
    }
}
